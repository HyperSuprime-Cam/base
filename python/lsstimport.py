import sys
import imp
import functools
import os.path

#List of extensions to set global flags.  Mayb need to be extended
#for systems other than *nix and OSX.
SHAREDEXTENSIONLIST = ('.so', '.dylib')
LIBEXCEPTIONLIST = ('_lsstcppimport.so',)

# Ensure that duplicate allocations--particularly those related to RTTI--are
# resolved by setting dynamical library loading flags.
RTLD_GLOBAL = -1
RTLD_NOW = -1
try:
    import dl
    if hasattr(dl, 'RTLD_GLOBAL'):  RTLD_GLOBAL = dl.RTLD_GLOBAL
    if hasattr(dl, 'RTLD_NOW'):     RTLD_NOW    = dl.RTLD_NOW
except ImportError:
    # 64bit linux does not have a dl module...
    pass
except SystemError:
    # ...if it does it should throw a SystemError
    pass

try:
    if RTLD_GLOBAL < 0:
        import lsst64defs
        RTLD_GLOBAL = lsst64defs.RTLD_GLOBAL   # usually 0x00100
    if RTLD_NOW < 0:
        import lsst64defs
        RTLD_NOW = lsst64defs.RTLD_NOW         # usually 0x00002
    DLFLAGS = RTLD_GLOBAL|RTLD_NOW
except ImportError:
    sys.stderr.write(
        "Could not import lsst64defs; please ensure the base package has been built (not just setup).\n"
    )

#Swigged python libraries that import other swigged python libraries need to import with RTLD_GLOBAL
#and RTLD_NOW set.  This causes problems with symbol collisions in third party packages (notably scipy).
#This cannot be fixed by using import hooks because python code generated by swig uses imp.load_module
#rather than import.  This makes it necessary to over ride imp.load_module.
#This was handled in ticket #3055:
#https://dev.lsstcorp.org/trac/ticket/3055
def setFlagsAndLoad(name, flags, *args):
    #Get currently set flags
    ORIGDLFLAGS = sys.getdlopenflags()
    #Set flags
    sys.setdlopenflags(DLFLAGS)
    try:
        module = orig_imp_load_module(name, *args)
    finally:
        #Set original flags
        sys.setdlopenflags(ORIGDLFLAGS)
    return module

orig_imp_load_module = imp.load_module
@functools.wraps(orig_imp_load_module)
def imp_load_module(name, *args):
    pathParts = args[1].split(os.path.sep)
    extension = os.path.splitext(pathParts[-1])[-1]
    #Find all swigged LSST libs.  Load _lsstcppimport.so by adding it to the EXCEPTIONLIST
    #since it may not have lsst in the path (it's in $BASE_DIR/python, not $BASE_DIR/python/lsst).
    if pathParts[-1] in LIBEXCEPTIONLIST or ('lsst' in pathParts[:-1] and pathParts[-1].startswith('_') and \
            extension in SHAREDEXTENSIONLIST):
        module = setFlagsAndLoad(name, DLFLAGS, *args)
    else:
        module = orig_imp_load_module(name, *args)
    return module
imp.load_module = imp_load_module

try:
    import lsstcppimport
except ImportError:
        sys.stderr.write(
        "Could not import lsstcppimport; please ensure the base package has been built (not just setup).\n"
    )
